using UnityEngine;
using UnityEngine.SceneManagement;

public class GameManager : MonoBehaviour
{
    public static GameManager Instance { get; private set; }

    [Header("Game Settings")]
    public int startMoney = 50;

    [Header("Spawning")]
    public GameObject enemyPrefab;
    public float spawnInterval = 2f;
    public int maxEnemiesOnWave = 10;

    [Header("References")]
    public Gate targetGate;
    public PathManager pathManager;

    // Game state
    private int _currentMoney;
    private float _spawnTimer;
    private int _enemiesSpawned;
    private bool _gameOver = false;

    void Awake()
    {
        // ????????
        if (Instance != null && Instance != this)
        {
            Destroy(gameObject);
            return;
        }
        Instance = this;
    }

    void Start()
    {
        _currentMoney = startMoney;
        _enemiesSpawned = 0;
        _gameOver = false;

        if (pathManager == null)
            pathManager = FindObjectOfType<PathManager>();

        if (targetGate == null)
            targetGate = FindObjectOfType<Gate>();

        Debug.Log("[GameManager] ???? ??????! ??????: " + _currentMoney + ". HP ????? ? ? UI.");
        Debug.Log("[GameManager] ?????????: ?????? ?? ????? ????? ??????? ????? (50 ?????)");
    }

    void Update()
    {
        if (_gameOver) return;


        if (UIManager.Instance != null && !UIManager.Instance.IsWaveActive())
            return;

        // ????????? ?????? ????? ?????? ?????????? (HP ????? = ???????????? ???????)
        if (targetGate == null || targetGate.currentHealth <= 0)
        {
            GameOver();
        }
    }

    void SpawnEnemy()
    {
        if (enemyPrefab == null)
        {
            Debug.LogError("[GameManager] Enemy prefab ?? ????????!");
            return;
        }

        GameObject go = Instantiate(enemyPrefab);
        TestEnemy enemy = go.GetComponent<TestEnemy>();
        if (enemy != null)
        {
            _enemiesSpawned++;
            Debug.Log($"[GameManager] ???? ???????! ????? ????????: {_enemiesSpawned}");
        }
    }

    /// <summary>
    /// ??????? ????????? ?????? (??? ????????? ?????)
    /// </summary>
    public bool TrySpendMoney(int amount)
    {
        if (_currentMoney < amount)
        {
            Debug.LogWarning($"[GameManager] ???????????? ?????! ????: {_currentMoney}, ?????: {amount}");
            return false;
        }

        _currentMoney -= amount;
        Debug.Log($"[GameManager] ????????? {amount} ?????. ????????: {_currentMoney}");
        return true;
    }

    /// <summary>
    /// ???????? ?????? (??? ???????? ?????)
    /// </summary>
    public void AddMoney(int amount)
    {
        _currentMoney += amount;
        Debug.Log($"[GameManager] +{amount} ?????! ?????: {_currentMoney}");
    }

    /// <summary>
    /// ???????? ??????? ?????????? ?????.
    /// </summary>
    public int GetMoney()
    {
        return _currentMoney;
    }

    /// <summary>
    /// ????????? ???? (?????????). ???????/???? ? ?????? ????? DefeatMenu.
    /// </summary>
    void GameOver()
    {
        _gameOver = true;
        Debug.Log("[GameManager] GAME OVER!");
    }

    /// <summary>
    /// ????????????? ???????
    /// </summary>
    public void RestartLevel()
    {
        SceneManager.LoadScene(SceneManager.GetActiveScene().buildIndex);
    }

    /// <summary>
    /// ????? ? ??????? ???? (???? ????)
    /// </summary>
    public void BackToMenu()
    {
        SceneManager.LoadScene(0); // ??? ??? ????? ????
    }

    /// <summary>
    /// ????? ?? ????
    /// </summary>
    public void QuitGame()
    {
#if UNITY_EDITOR
        UnityEditor.EditorApplication.isPlaying = false;
#else
        Application.Quit();
#endif
    }
}